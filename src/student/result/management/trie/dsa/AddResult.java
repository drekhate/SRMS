    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String roll = jTextField1.getText();
        String phy = jTextField2.getText();
        String math = jTextField3.getText();
        String em = jTextField4.getText();
        String dbms = jTextField5.getText();
        String os = jTextField6.getText();
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "8dipakR0@");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno = '"+roll+"'");
            if(rs.next()) {
                st.executeUpdate("insert into result(roll_no, Physics, math, em, dbms, os) value('"+roll+"', '"+phy+"', '"+math+"', '"+em+"', '"+dbms+"', '"+os+"')");
                JOptionPane.showMessageDialog(null, "Marks save successfully");
                setVisible(false);
                new AddResult().setVisible(true);
            }
            else {
                JOptionPane.showMessageDialog(null, "This roll no not register");
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

